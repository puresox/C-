#include<stdio.h>
int main()
{
	int i,j;
	int a[10]={1,2,3,4,5,10,15,20,50,100};
	printf("请输入一个数");
	scanf("%d",&i);
	if(i<=a[1])
	{
		printf("%d\n",i);
		for(j=0;j<=9;j++)
		{
			printf("%d\n",a[j]);
		}
	}
	else
	{
		for(j=0;j<=9;j++)
		{
			printf("%d\n",a[j]);
			if(i>=a[j]&&i<=a[j+1])
			{
			printf("%d\n",i);break;
			}
		}
		for(j=j+1;j<=9;j++)
		printf("%d\n",a[j]);
	}
	return 0;
}